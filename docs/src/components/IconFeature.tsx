interface IconFeatureProps {
  /** Icon name from the available icon set */
  icon: keyof typeof IconComponents;
  /** Feature title */
  title: string;
  /** Feature description */
  children: React.ReactNode;
}

/**
 * IconFeature Component
 * 
 * Used for feature grids on documentation pages.
 * Displays an icon, title, and description in a card format.
 * 
 * @example
 * ```mdx
 * <IconFeature icon="ShieldCheck" title="Privacy First">
 *   Mathematical privacy guarantees with differential privacy.
 * </IconFeature>
 * ```
 */
export function IconFeature({ icon, title, children }: IconFeatureProps): React.JSX.Element {
  const IconComponent = IconComponents[icon] || IconComponents.CheckCircle;
  
  return (
    <div className="icon-feature">
      <div className="icon-feature__icon">
        <IconComponent />
      </div>
      <div className="icon-feature__title">{title}</div>
      <div className="icon-feature__content">{children}</div>
    </div>
  );
}

/**
 * FeatureGrid Component
 * 
 * Wrapper for multiple IconFeature components.
 * Creates a responsive grid layout.
 */
export function FeatureGrid({ children }: { children: React.ReactNode }): React.JSX.Element {
  return <div className="feature-grid">{children}</div>;
}

export default IconFeature;
